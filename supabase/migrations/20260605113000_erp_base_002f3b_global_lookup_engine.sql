-- ============================================================================
-- TABLE: global_lookup_categories
-- ============================================================================
-- Stores lookup category definitions that can be used across the ERP

create table if not exists public.global_lookup_categories (
  id bigint generated by default as identity primary key,
  
  -- Category identification
  category_code text not null unique,
  category_name_en text not null,
  category_name_ar text,
  description text,
  module_code text,
  
  -- Scope and behavior
  category_scope text not null default 'GLOBAL' check (category_scope in ('GLOBAL', 'COMPANY', 'BRANCH', 'MODULE')),
  supports_hierarchy boolean not null default false,
  supports_color boolean not null default false,
  supports_icon boolean not null default false,
  supports_effective_dates boolean not null default false,
  supports_metadata boolean not null default true,
  
  -- Status and governance
  is_system boolean not null default false,
  is_locked boolean not null default false,
  is_active boolean not null default true,
  sort_order integer not null default 0,
  
  -- Audit fields
  created_at timestamptz not null default now(),
  created_by bigint,
  updated_at timestamptz not null default now(),
  updated_by bigint,
  deactivated_at timestamptz,
  deactivated_by bigint,
  deactivation_reason text,
  
  -- Constraints
  check (category_code = upper(category_code)),
  check (category_code ~ '^[A-Z0-9_]+$'),
  check (
    (is_active = true and deactivated_at is null) or
    (is_active = false and deactivated_at is not null)
  )
);

-- Comments
comment on table public.global_lookup_categories is 'Master data lookup categories for standardized ERP dropdowns';
comment on column public.global_lookup_categories.category_code is 'Unique uppercase code for programmatic reference (e.g., STATUS_TYPES)';
comment on column public.global_lookup_categories.category_scope is 'GLOBAL = enterprise-wide, COMPANY = per-company, BRANCH = per-branch, MODULE = module-specific';
comment on column public.global_lookup_categories.supports_hierarchy is 'True if values can have parent-child relationships';
comment on column public.global_lookup_categories.supports_color is 'True if values can have color metadata (e.g., for status badges)';
comment on column public.global_lookup_categories.supports_icon is 'True if values can have icon metadata';
comment on column public.global_lookup_categories.supports_effective_dates is 'True if values can have effective date ranges';
comment on column public.global_lookup_categories.is_system is 'True if category is system-managed and critical';
comment on column public.global_lookup_categories.is_locked is 'True if category cannot be modified (requires special permission)';

-- Indexes
create index if not exists idx_global_lookup_categories_code on public.global_lookup_categories(category_code);
create index if not exists idx_global_lookup_categories_module on public.global_lookup_categories(module_code) where module_code is not null;
create index if not exists idx_global_lookup_categories_active on public.global_lookup_categories(is_active);
create index if not exists idx_global_lookup_categories_system on public.global_lookup_categories(is_system);
create index if not exists idx_global_lookup_categories_locked on public.global_lookup_categories(is_locked);
create index if not exists idx_global_lookup_categories_sort on public.global_lookup_categories(sort_order);

-- Trigger for updated_at
create trigger trigger_global_lookup_categories_updated_at
  before update on public.global_lookup_categories
  for each row
  execute function public.set_updated_at();

-- ============================================================================
-- TABLE: global_lookup_values
-- ============================================================================
-- Stores individual lookup values within categories

create table if not exists public.global_lookup_values (
  id bigint generated by default as identity primary key,
  
  -- Category reference
  category_id bigint not null references public.global_lookup_categories(id) on delete restrict,
  
  -- Value identification
  value_code text not null,
  value_label_en text not null,
  value_label_ar text,
  description text,
  
  -- Hierarchy
  parent_value_id bigint references public.global_lookup_values(id) on delete restrict,
  
  -- Display metadata
  color_hex text,
  icon_name text,
  badge_variant text,
  sort_order integer not null default 0,
  
  -- Value properties
  is_default boolean not null default false,
  is_system boolean not null default false,
  is_locked boolean not null default false,
  is_active boolean not null default true,
  
  -- Effective dates
  effective_from date,
  effective_to date,
  
  -- Extensibility
  metadata_json jsonb not null default '{}'::jsonb,
  
  -- Audit fields
  created_at timestamptz not null default now(),
  created_by bigint,
  updated_at timestamptz not null default now(),
  updated_by bigint,
  deactivated_at timestamptz,
  deactivated_by bigint,
  deactivation_reason text,
  
  -- Constraints
  unique(category_id, value_code),
  check (value_code = upper(value_code)),
  check (value_code ~ '^[A-Z0-9_]+$'),
  check (parent_value_id is null or parent_value_id != id),
  check (color_hex is null or color_hex ~ '^#[0-9A-Fa-f]{6}$'),
  check (effective_to is null or effective_from is null or effective_to >= effective_from),
  check (
    (is_active = true and deactivated_at is null) or
    (is_active = false and deactivated_at is not null)
  )
);

-- Comments
comment on table public.global_lookup_values is 'Individual lookup values within categories';
comment on column public.global_lookup_values.value_code is 'Unique code within category (e.g., ACTIVE, PENDING)';
comment on column public.global_lookup_values.parent_value_id is 'Parent value ID for hierarchical lookups (must be same category)';
comment on column public.global_lookup_values.color_hex is 'Color for UI badges (e.g., #22C55E for success/active)';
comment on column public.global_lookup_values.icon_name is 'Icon name from UI icon library';
comment on column public.global_lookup_values.badge_variant is 'Badge variant for UI (e.g., success, warning, danger)';
comment on column public.global_lookup_values.is_default is 'True if this is the default value for new records';
comment on column public.global_lookup_values.effective_from is 'Value becomes active on this date';
comment on column public.global_lookup_values.effective_to is 'Value becomes inactive after this date';
comment on column public.global_lookup_values.metadata_json is 'Flexible JSONB for module-specific metadata';

