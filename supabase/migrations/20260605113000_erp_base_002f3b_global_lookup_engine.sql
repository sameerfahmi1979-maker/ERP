-- ============================================================================
-- VALIDATION FUNCTION: Check parent value belongs to same category
-- ============================================================================

create or replace function public.validate_lookup_value_parent()
returns trigger
language plpgsql
as $$
begin
  -- Check that parent value belongs to the same category
  if new.parent_value_id is not null then
    if not exists (
      select 1 from public.global_lookup_values
      where id = new.parent_value_id
      and category_id = new.category_id
    ) then
      raise exception 'Parent value must belong to the same category';
    end if;
  end if;
  
  return new;
end;
$$;

-- Trigger for parent validation
create trigger trigger_validate_lookup_value_parent
  before insert or update on public.global_lookup_values
  for each row
  execute function public.validate_lookup_value_parent();

