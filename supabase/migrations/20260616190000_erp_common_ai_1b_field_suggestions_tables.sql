-- ============================================================================
-- SECTION 5: Explicit Pilot Entity Permission Helper Functions
--
-- Rules:
-- - Explicit CASE mapping only — no dynamic SQL
-- - Return false for any entity_type outside company/party/branch/site
-- - Uses existing current_user_has_permission()
-- - Checks entity row exists (soft-delete aware)
-- ============================================================================

CREATE OR REPLACE FUNCTION public.current_user_can_view_ai_entity(
  p_entity_type TEXT,
  p_entity_id   BIGINT
)
RETURNS BOOLEAN
LANGUAGE plpgsql
STABLE
SECURITY INVOKER
AS $$
DECLARE
  v_has_permission BOOLEAN := false;
  v_entity_exists  BOOLEAN := false;
BEGIN
  -- Map entity_type to required view permission
  CASE p_entity_type
    WHEN 'company' THEN
      v_has_permission := current_user_has_permission('organizations.view')
                          OR current_user_has_role('system_admin');
      SELECT EXISTS(
        SELECT 1 FROM public.owner_companies
        WHERE id = p_entity_id AND deleted_at IS NULL
      ) INTO v_entity_exists;

    WHEN 'party' THEN
      v_has_permission := current_user_has_permission('master_data.party_master.view')
                          OR current_user_has_role('system_admin');
      SELECT EXISTS(
        SELECT 1 FROM public.parties
        WHERE id = p_entity_id AND deleted_at IS NULL
      ) INTO v_entity_exists;

    WHEN 'branch' THEN
      v_has_permission := current_user_has_permission('branches.view')
                          OR current_user_has_role('system_admin');
      SELECT EXISTS(
        SELECT 1 FROM public.branches
        WHERE id = p_entity_id AND deleted_at IS NULL
      ) INTO v_entity_exists;

    WHEN 'site' THEN
      v_has_permission := current_user_has_permission('common_md.work_sites.view')
                          OR current_user_has_role('system_admin');
      SELECT EXISTS(
        SELECT 1 FROM public.work_sites
        WHERE id = p_entity_id AND deleted_at IS NULL
      ) INTO v_entity_exists;

    ELSE
      -- Unregistered entity type — always deny
      RETURN false;
  END CASE;

  RETURN v_has_permission AND v_entity_exists;
END;
$$;

COMMENT ON FUNCTION public.current_user_can_view_ai_entity(TEXT, BIGINT) IS
  'Common AI.1B — Returns true if the current user has view permission for the given AI entity type and the entity row exists. '
  'Explicit CASE mapping only — no dynamic SQL. Returns false for unregistered entity types.';

