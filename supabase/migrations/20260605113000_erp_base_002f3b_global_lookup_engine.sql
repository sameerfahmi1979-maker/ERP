-- Assign permissions to roles
do $$
declare
  v_system_admin_role_id bigint;
  v_group_admin_role_id bigint;
  v_company_admin_role_id bigint;
  v_branch_admin_role_id bigint;
  v_permission_id bigint;
begin
  -- Get role IDs
  select id into v_system_admin_role_id from public.roles where role_code = 'system_admin';
  select id into v_group_admin_role_id from public.roles where role_code = 'group_admin';
  select id into v_company_admin_role_id from public.roles where role_code = 'company_admin';
  select id into v_branch_admin_role_id from public.roles where role_code = 'branch_admin';
  
  -- system_admin: all 7 permissions
  for v_permission_id in
    select id from public.permissions
    where permission_code like 'master_data.%'
  loop
    insert into public.role_permissions (role_id, permission_id)
    values (v_system_admin_role_id, v_permission_id)
    on conflict (role_id, permission_id) do nothing;
  end loop;
  
  -- group_admin: dashboard, view, manage, export, audit_view
  for v_permission_id in
    select id from public.permissions
    where permission_code in (
      'master_data.dashboard.view',
      'master_data.lookups.view',
      'master_data.lookups.manage',
      'master_data.lookups.export',
      'master_data.lookups.audit_view'
    )
  loop
    insert into public.role_permissions (role_id, permission_id)
    values (v_group_admin_role_id, v_permission_id)
    on conflict (role_id, permission_id) do nothing;
  end loop;
  
  -- company_admin: dashboard, view, export
  for v_permission_id in
    select id from public.permissions
    where permission_code in (
      'master_data.dashboard.view',
      'master_data.lookups.view',
      'master_data.lookups.export'
    )
  loop
    insert into public.role_permissions (role_id, permission_id)
    values (v_company_admin_role_id, v_permission_id)
    on conflict (role_id, permission_id) do nothing;
  end loop;
  
  -- branch_admin: view only
  select id into v_permission_id from public.permissions where permission_code = 'master_data.lookups.view';
  insert into public.role_permissions (role_id, permission_id)
  values (v_branch_admin_role_id, v_permission_id)
  on conflict (role_id, permission_id) do nothing;
end $$;

