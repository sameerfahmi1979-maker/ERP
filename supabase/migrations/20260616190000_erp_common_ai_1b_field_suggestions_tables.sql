CREATE TABLE IF NOT EXISTS public.erp_ai_field_suggestions (
  id                    BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,

  -- Target entity (pilot: company | party | branch | site)
  entity_type           TEXT NOT NULL,
  entity_id             BIGINT NOT NULL,

  -- Target field
  target_table          TEXT NOT NULL,
  target_field          TEXT NOT NULL,
  field_label           TEXT NOT NULL,
  field_type            TEXT NOT NULL,

  -- Values
  current_value         TEXT,
  suggested_value       TEXT,
  suggested_value_json  JSONB,

  -- AI metadata
  suggestion_type       TEXT NOT NULL,
  confidence_score      NUMERIC(5,4),

  -- Evidence references (links to DMS)
  source_document_id    BIGINT REFERENCES public.dms_documents(id) ON DELETE SET NULL,
  source_file_id        BIGINT REFERENCES public.dms_document_files(id) ON DELETE SET NULL,
  source_document_type  TEXT,
  source_excerpt        TEXT,
  ai_reason             TEXT,

  -- Lifecycle
  status                TEXT NOT NULL DEFAULT 'pending',

  -- Human action tracking
  accepted_by           BIGINT REFERENCES public.user_profiles(id) ON DELETE SET NULL,
  accepted_at           TIMESTAMPTZ,
  rejected_by           BIGINT REFERENCES public.user_profiles(id) ON DELETE SET NULL,
  rejected_at           TIMESTAMPTZ,
  applied_by            BIGINT REFERENCES public.user_profiles(id) ON DELETE SET NULL,
  applied_at            TIMESTAMPTZ,

  -- Error tracking
  apply_error           TEXT,

  -- Generation metadata
  generation_batch_id   BIGINT,
  prompt_version        TEXT,

  -- Standard audit columns
  created_at            TIMESTAMPTZ NOT NULL DEFAULT now(),
  created_by            BIGINT REFERENCES public.user_profiles(id) ON DELETE SET NULL,
  updated_at            TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_by            BIGINT REFERENCES public.user_profiles(id) ON DELETE SET NULL,
  deleted_at            TIMESTAMPTZ,

  -- Constraints
  CONSTRAINT erp_ai_field_suggestions_entity_type_chk CHECK (
    entity_type IN ('company','party','branch','site')
  ),
  CONSTRAINT erp_ai_field_suggestions_field_type_chk CHECK (
    field_type IN ('text','date','number','boolean','fk','json')
  ),
  CONSTRAINT erp_ai_field_suggestions_type_chk CHECK (
    suggestion_type IN (
      'fill_missing','correct_value','update_existing',
      'clear_wrong_value','conflict_detected','needs_human_review'
    )
  ),
  CONSTRAINT erp_ai_field_suggestions_status_chk CHECK (
    status IN ('pending','accepted','rejected','superseded','applied','failed')
  ),
  CONSTRAINT erp_ai_field_suggestions_confidence_chk CHECK (
    confidence_score IS NULL
    OR (confidence_score >= 0 AND confidence_score <= 1)
  ),
  CONSTRAINT erp_ai_field_suggestions_excerpt_len_chk CHECK (
    source_excerpt IS NULL OR char_length(source_excerpt) <= 500
  ),
  CONSTRAINT erp_ai_field_suggestions_reason_len_chk CHECK (
    ai_reason IS NULL OR char_length(ai_reason) <= 1000
  )
);

COMMENT ON TABLE public.erp_ai_field_suggestions IS
  'Common AI.1B — Field suggestion records generated by the Universal Internal AI Engine. '
  'All suggestions require human review before apply. No auto-apply.';

-- Indexes
