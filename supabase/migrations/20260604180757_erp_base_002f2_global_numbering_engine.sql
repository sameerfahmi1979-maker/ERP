-- ============================================================================
-- ERP BASE 002F.2 — Global Numbering Engine
-- ============================================================================
-- Created: 2026-06-04
-- Purpose: Simple ERP reference number generation (EMP-0001, PO-0025, INV-0312)
-- Format: {DOC}-{SEQ4} (No company/branch/city/year/month codes)
-- ============================================================================

-- ============================================================================
-- TABLE: global_numbering_rules
-- ============================================================================
-- Stores configuration for document numbering sequences

create table if not exists public.global_numbering_rules (
  id bigint generated by default as identity primary key,
  
  -- Rule identification
  rule_code text not null unique,
  rule_name text not null,
  description text,
  
  -- Module and document type
  module_code text not null,
  module_name text not null,
  document_type_code text not null,
  document_type_name text not null,
  document_prefix text not null,
  
  -- Format configuration
  separator text not null default '-',
  format_template text not null default '{DOC}-{SEQ4}',
  sequence_length int not null default 4 check (sequence_length between 1 and 12),
  padding_character text not null default '0',
  
  -- Sequence state
  starting_sequence_number bigint not null default 1 check (starting_sequence_number >= 1),
  current_sequence_number bigint not null default 0 check (current_sequence_number >= 0),
  next_sequence_number bigint not null default 1 check (next_sequence_number >= 1),
  
  -- Reset policy
  reset_policy text not null default 'never' check (reset_policy in ('never', 'yearly', 'monthly')),
  
  -- Generation policies
  reserve_on_draft boolean not null default false,
  reserve_on_submit boolean not null default true,
  allow_manual_override boolean not null default false,
  manual_override_requires_permission boolean not null default true,
  allow_gaps boolean not null default true,
  cancelled_number_policy text not null default 'never_reuse' check (cancelled_number_policy in ('never_reuse', 'allow_reuse')),
  duplicate_prevention_scope text not null default 'document_type',
  
  -- Status
  is_active boolean not null default true,
  is_locked boolean not null default false,
  effective_from timestamptz,
  effective_to timestamptz,
  
  -- Audit
  notes text,
  created_at timestamptz not null default now(),
  created_by bigint,
  updated_at timestamptz not null default now(),
  updated_by bigint,
  
  -- Constraints
  check (document_prefix = upper(document_prefix)),
  check (document_prefix ~ '^[A-Z0-9_]+$'),
  check (format_template like '%{DOC}%'),
  check (format_template ~ '\{SEQ[0-9]*\}'),
  check (effective_to is null or effective_to >= effective_from)
);

comment on table public.global_numbering_rules is 'Configuration for simple ERP reference number generation (EMP-0001, PO-0025)';
comment on column public.global_numbering_rules.format_template is 'Template tokens: {DOC}, {SEQ}, {SEQ3}, {SEQ4}, {SEQ5}, {SEQ6}. Example: {DOC}-{SEQ4}';
comment on column public.global_numbering_rules.reset_policy is 'never = continuous sequence, yearly/monthly = future enhancement';

-- ============================================================================
-- TABLE: global_numbering_sequence_states
-- ============================================================================
-- Tracks actual sequence state (separate for concurrency safety)

create table if not exists public.global_numbering_sequence_states (
  id bigint generated by default as identity primary key,
  
  -- Link to rule
  numbering_rule_id bigint not null references public.global_numbering_rules(id) on delete restrict,
  
  -- Document identification
  module_code text not null,
  document_type_code text not null,
  document_prefix text not null,
  
  -- Reset period key for future yearly/monthly resets
  reset_period_key text not null default 'GLOBAL',
  
  -- Sequence tracking
  last_sequence_number bigint not null default 0 check (last_sequence_number >= 0),
  next_sequence_number bigint not null default 1 check (next_sequence_number >= 1),
  last_generated_reference text,
  last_generated_at timestamptz,
  
  -- Audit
  created_at timestamptz not null default now(),
  created_by bigint,
  updated_at timestamptz not null default now(),
  updated_by bigint,
  
  -- Constraints
  unique (numbering_rule_id, reset_period_key)
);

comment on table public.global_numbering_sequence_states is 'Actual sequence state for concurrency-safe number generation';

-- ============================================================================
-- TABLE: global_numbering_generated_references
-- ============================================================================
-- Audit trail of all generated/reserved/consumed/cancelled numbers

create table if not exists public.global_numbering_generated_references (
  id bigint generated by default as identity primary key,
  
  -- Links
  numbering_rule_id bigint not null references public.global_numbering_rules(id) on delete restrict,
  sequence_state_id bigint references public.global_numbering_sequence_states(id) on delete restrict,
  
  -- Generated reference
  generated_reference_number text not null unique,
  generated_sequence_number bigint not null check (generated_sequence_number >= 1),
  
  -- Document identification
  module_code text not null,
  document_type_code text not null,
  document_prefix text not null,
  
  -- Target record linkage
  target_table_name text,
  target_record_id bigint,
  
  -- Status tracking
  generation_status text not null default 'consumed' check (
    generation_status in ('preview_only', 'reserved', 'consumed', 'cancelled', 'manual_override')
  ),
  generation_reason text,
  
  -- Lifecycle timestamps
  reserved_at timestamptz,
  consumed_at timestamptz,
  cancelled_at timestamptz,
  cancelled_reason text,
  
  -- Manual override tracking
  manual_override_used boolean not null default false,
  manual_override_reason text,
  
  -- Audit
  generated_by bigint,
  generated_at timestamptz not null default now(),
  created_at timestamptz not null default now(),
  created_by bigint,
  updated_at timestamptz not null default now(),
  updated_by bigint
);

comment on table public.global_numbering_generated_references is 'Complete audit trail of all generated reference numbers';

