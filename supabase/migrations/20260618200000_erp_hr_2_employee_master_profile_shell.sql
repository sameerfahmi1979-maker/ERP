CREATE POLICY employees_select
  ON employees FOR SELECT
  USING (
    current_user_is_global_admin()
    OR current_user_has_permission_in_company('hr.employees.view', owner_company_id)
    OR current_user_has_permission_in_company('hr.employee_profile.view', owner_company_id)
  );

CREATE POLICY employees_insert
  ON employees FOR INSERT
  WITH CHECK (
    current_user_is_global_admin()
    OR current_user_has_permission('hr.employees.create')
  );

CREATE POLICY employees_update
  ON employees FOR UPDATE
  USING (
    current_user_is_global_admin()
    OR current_user_has_permission_in_company('hr.employees.update', owner_company_id)
    OR current_user_has_permission_in_company('hr.employee_profile.manage', owner_company_id)
  )
  WITH CHECK (
    current_user_is_global_admin()
    OR current_user_has_permission_in_company('hr.employees.update', owner_company_id)
