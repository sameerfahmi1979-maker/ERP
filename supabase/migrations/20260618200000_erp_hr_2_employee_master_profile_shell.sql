CREATE OR REPLACE FUNCTION current_user_can_view_employee(p_employee_id BIGINT)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
AS $$
  SELECT (
    current_user_is_global_admin()
    OR EXISTS (
      SELECT 1 FROM employees e
      WHERE e.id = p_employee_id
        AND current_user_has_permission_in_company('hr.employees.view', e.owner_company_id)
    )
    OR EXISTS (
      SELECT 1 FROM employees e
      WHERE e.id = p_employee_id
        AND current_user_has_permission_in_company('hr.employee_profile.view', e.owner_company_id)
    )
  );
$$;

CREATE OR REPLACE FUNCTION current_user_can_manage_employee(p_employee_id BIGINT)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
AS $$
  SELECT (
    current_user_is_global_admin()
    OR EXISTS (
      SELECT 1 FROM employees e
      WHERE e.id = p_employee_id
        AND current_user_has_permission_in_company('hr.employees.update', e.owner_company_id)
    )
    OR EXISTS (
      SELECT 1 FROM employees e
      WHERE e.id = p_employee_id
        AND current_user_has_permission_in_company('hr.employee_profile.manage', e.owner_company_id)
    )
  );
$$;

