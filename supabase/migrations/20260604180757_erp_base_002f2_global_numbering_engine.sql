create policy "Allow viewing generated references for authenticated users"
  on public.global_numbering_generated_references
  for select
  using (true);

-- Policy: Generated references managed only by functions
create policy "Generated references managed by functions only"
  on public.global_numbering_generated_references
  for all
  using (false)
  with check (false);

